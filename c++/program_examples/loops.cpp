#include<iostream>
using namespace std;

/* for loop in c++*/
int main(){
    
    for(int i=0; i<=100; i++){
        cout<<i<<endl;
    }
    int a=1;
    while(a<40){
        cout<<a<<endl;
        a++;
    }
    int b=1;
    do{
        cout<<b<<endl;
        b++;
    }while(b<40);
    return(0);
}